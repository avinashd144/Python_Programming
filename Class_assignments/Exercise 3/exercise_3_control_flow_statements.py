"""18. Write a function to generate a list of prime numbers up to a given limit.

"""

def generate_primes(limit):
  primes = []
  for num in range(2, limit ++ 1):
    for i in range(2, num):
      if (num % i) == 0:
        break
    else:
      primes.append(num)
  return primes
