"""4. Write a program to print the Fibonacci sequence up to a given number of terms."""

def fibonacci(n):
  a, b = 0, 1
  fib = [a, b]
  for i in range(n - 2) :
    a, b = b, a + b
    fib.append(b)
  return fib
terms = int(input("Enter the number of terms: "))
print("Fibonacci sequence:", fibonacci(terms))

